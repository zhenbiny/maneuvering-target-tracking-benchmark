function entry = make_entry(cfg, id, name, runner, category, color, line_style, marker, ...
    supports_acceleration, supports_model_probabilities, supports_mode_switching, supports_input_estimation)
style = struct( ...
    'color', color, ...
    'line_style', line_style, ...
    'marker', marker);

entry = struct( ...
    'id', id, ...
    'name', name, ...
    'runner', runner, ...
    'category', category, ...
    'enabled', cfg.algorithms.(id).enabled, ...
    'style', style, ...
    'supports_acceleration', supports_acceleration, ...
    'supports_model_probabilities', supports_model_probabilities, ...
    'supports_mode_switching', supports_mode_switching, ...
    'supports_input_estimation', supports_input_estimation);
end
